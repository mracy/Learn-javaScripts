let text = "I will grab the all opportunities.";
const myArray = text.split(" ");

console.log(myArray);

console.log("Array length: ", myArray.length);

console.log("First Element: ", myArray[0]);
console.log("Last Element: ", myArray[myArray.length - 1]);

 const newText = myArray.join(" ");
 console.log("The joined string: ", newText);

 const reversedArray = myArray.reverse();
 console.log("Reversed Array: ", reversedArray);

 const reversedText = myArray.join(" ");
console.log("Reversed Text: ", reversedText);


